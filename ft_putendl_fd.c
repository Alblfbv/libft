#include "libft.h"

void	ft_putendl_fd(char const *str, int fd)
{
	int	i;

	if (!(str))
		return ;
	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar_fd(str[i], fd);
		i++;
	}
	ft_putchar_fd('\n', fd);
}
