int	ft_atoi(const char *s)
{
	int	i;
	int	sign;
	int	nb;

	i = 0;
	sign = 1;
	nb = 0;
	while (s[i] < 27 || s[i] == '\n' || s[i] == '\t'
			|| s[i] == '\r' || s[i] == '\r'
			|| s[i] == '\v' || s[i] == 32 || s[i] == '\f')
		i++;
	if (s[i] == '+' || s[i] == '-')
	{
		if (s[i] == '-')
			sign = -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		nb = nb * 10 + s[i] - 48;
		i++;
	}
	return (nb * sign);
}
